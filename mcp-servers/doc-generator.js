#!/usr/bin/env node

/**
 * doc-generator MCP Server
 *
 * 역할: DESIGN.md 기반으로 자동으로 문서 생성
 * 기능:
 *   - API 문서 생성
 *   - README 업데이트
 *   - 변경 사항 정리
 */

const fs = require('fs');
const path = require('path');

// ============================================
// 1. API 문서 생성
// ============================================

function generateApiDoc() {
  try {
    const designPath = path.join(process.cwd(), 'docs/02-design/DESIGN.md');
    const designContent = fs.readFileSync(designPath, 'utf-8');

    // DESIGN.md에서 API 섹션 추출
    const apiMatch = designContent.match(/## 🔌 API 명세([\s\S]*?)---/);
    if (!apiMatch) {
      return {
        success: false,
        error: 'API 명세를 찾을 수 없습니다'
      };
    }

    const apiDoc = `# API 문서

자동 생성됨: ${new Date().toISOString()}

${apiMatch[1]}

## cURL 예제

### 할 일 생성
\`\`\`bash
curl -X POST http://localhost:3001/api/todos \\
  -H "Content-Type: application/json" \\
  -d '{
    "title": "새로운 할 일",
    "priority": "HIGH",
    "dueDate": "2026-03-20"
  }'
\`\`\`

### 할 일 목록 조회
\`\`\`bash
curl http://localhost:3001/api/todos?priority=HIGH
\`\`\`

### 할 일 수정
\`\`\`bash
curl -X PATCH http://localhost:3001/api/todos/1 \\
  -H "Content-Type: application/json" \\
  -d '{"completed": true}'
\`\`\`

### 할 일 삭제
\`\`\`bash
curl -X DELETE http://localhost:3001/api/todos/1
\`\`\`

---

생성 시간: ${new Date().toLocaleString()}
`;

    return {
      success: true,
      doc: apiDoc,
      filename: 'API.md',
      message: 'API 문서가 생성되었습니다'
    };
  } catch (error) {
    return {
      success: false,
      error: error.message
    };
  }
}

// ============================================
// 2. README 업데이트
// ============================================

function updateReadme() {
  try {
    const readmePath = path.join(process.cwd(), 'README.md');
    let readmeContent = fs.readFileSync(readmePath, 'utf-8');

    // 마지막 업데이트 날짜 수정
    const now = new Date().toLocaleString();
    readmeContent = readmeContent.replace(
      /\*\*마지막 업데이트\*\*: .*/,
      `**마지막 업데이트**: ${now}`
    );

    // 변경 사항 섹션 추가
    const changelogSection = `

## 📝 최근 변경 사항 (${new Date().toISOString().split('T')[0]})

- ✅ API 문서 자동 생성 완료
- ✅ doc-generator MCP 서버 추가
- ✅ README 자동 업데이트 기능 추가

이전 변경 사항은 [Git 히스토리](https://github.com) 참조`;

    const updatedReadme = readmeContent + changelogSection;

    return {
      success: true,
      content: updatedReadme,
      message: 'README가 업데이트되었습니다'
    };
  } catch (error) {
    return {
      success: false,
      error: error.message
    };
  }
}

// ============================================
// 3. 변경 사항 요약
// ============================================

function generateChangelog() {
  const changelogContent = `# Changelog

## [1.0.0] - ${new Date().toISOString().split('T')[0]}

### Added
- ✨ Todo CRUD API 구현
- 🗄️ Prisma + SQLite 데이터베이스
- 📝 자동 문서 생성 (doc-generator MCP)
- 🛠️ npm-scripts MCP 서버
- 📚 PDCA 문서 체계

### Features
- GET /api/todos - 할 일 목록 조회
- POST /api/todos - 할 일 생성
- PATCH /api/todos/:id - 할 일 수정
- DELETE /api/todos/:id - 할 일 삭제

### Technical Stack
- Backend: Express.js + Prisma
- Database: SQLite
- Frontend: Next.js (예정)
- Tools: MCP (Model Context Protocol)

### Known Issues
- None

---

Generated by doc-generator MCP
`;

  return {
    success: true,
    content: changelogContent,
    filename: 'CHANGELOG.md',
    message: 'Changelog가 생성되었습니다'
  };
}

// ============================================
// 4. MCP 도구 정의
// ============================================

const tools = [
  {
    name: 'generate_api_doc',
    description: 'DESIGN.md 기반으로 API 문서 생성',
    handler: generateApiDoc
  },
  {
    name: 'update_readme',
    description: 'README.md 자동 업데이트',
    handler: updateReadme
  },
  {
    name: 'generate_changelog',
    description: 'Changelog 생성',
    handler: generateChangelog
  }
];

// ============================================
// 5. 간단한 HTTP 서버
// ============================================

const http = require('http');

const server = http.createServer(async (req, res) => {
  const url = new URL(req.url, `http://${req.headers.host}`);

  res.setHeader('Content-Type', 'application/json');

  try {
    if (url.pathname === '/tools') {
      res.writeHead(200);
      res.end(JSON.stringify({
        tools: tools.map(t => ({
          name: t.name,
          description: t.description
        }))
      }, null, 2));

    } else if (url.pathname === '/generate-api-doc') {
      const result = generateApiDoc();
      res.writeHead(result.success ? 200 : 400);
      res.end(JSON.stringify(result, null, 2));

    } else if (url.pathname === '/update-readme') {
      const result = updateReadme();
      res.writeHead(result.success ? 200 : 400);
      res.end(JSON.stringify(result, null, 2));

    } else if (url.pathname === '/generate-changelog') {
      const result = generateChangelog();
      res.writeHead(result.success ? 200 : 400);
      res.end(JSON.stringify(result, null, 2));

    } else if (url.pathname === '/health') {
      res.writeHead(200);
      res.end(JSON.stringify({
        status: 'ok',
        server: 'doc-generator-mcp',
        timestamp: new Date().toISOString()
      }));

    } else {
      res.writeHead(404);
      res.end(JSON.stringify({ error: 'Not found' }));
    }
  } catch (error) {
    res.writeHead(500);
    res.end(JSON.stringify({ error: error.message }));
  }
});

// ============================================
// 6. 서버 시작
// ============================================

const PORT = process.env.DOC_PORT || 3002;

server.listen(PORT, () => {
  console.log(`\n✅ doc-generator MCP 서버 시작됨`);
  console.log(`📍 포트: ${PORT}`);
  console.log(`🔗 URL: http://localhost:${PORT}`);
  console.log(`\n📚 사용 가능한 엔드포인트:`);
  console.log(`   - GET /tools`);
  console.log(`   - GET /generate-api-doc`);
  console.log(`   - GET /update-readme`);
  console.log(`   - GET /generate-changelog`);
  console.log(`   - GET /health`);
  console.log(`\n`);
});

process.on('SIGTERM', () => {
  console.log('\n⛔ 서버 종료');
  server.close();
});

process.on('SIGINT', () => {
  console.log('\n⛔ 서버 종료');
  server.close();
});
